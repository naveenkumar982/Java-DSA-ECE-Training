package com.tripillar_coding_assignments.basics_and_operators;

public class MathematicalOperations {
    public static void main(String[] args) {
        int a=20;
        int b=10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}
