package com.basics;

public class DataTypes {
    public static void main(String[] args) {
        System.out.println(args[0]);
        System.out.println(args[1]);
        System.out.println(args[2]);
        System.out.println("byte range:"+Byte.MIN_VALUE+" to "+Byte.MAX_VALUE);
        System.out.println("byte range:"+Short.MIN_VALUE+" to "+Short.MAX_VALUE);
        System.out.println("byte range:"+Integer.MIN_VALUE+" to "+Integer.MAX_VALUE);
        System.out.println("byte range:"+Long.MIN_VALUE+" to "+Long.MAX_VALUE);
        System.out.println("byte range:"+Float.MIN_VALUE+" to "+Float.MAX_VALUE);
        System.out.println("byte range:"+Double.MIN_VALUE+" to "+Double.MAX_VALUE);
        System.out.println("byte range:"+(int)Character.MIN_VALUE+" to "+(int)Character.MAX_VALUE);
    }
}
