package com.basics;

public class BitwiseOperators {
    static void main(String[] args) {
        System.out.println(7 &5);
        System.out.println(6 ^ 9);
        System.out.println(8 | 4);
        System.out.println(10<<2);
        System.out.println(10>> 2);
        System.out.println(~5);
    }
}
